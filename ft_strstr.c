char	*ft_strstr(const char *s1, const char *s2)
{
	int		i;
	int		j;
	int		k;

	i = 0;
	k = 0;
	if (s2[i] == '\0')
		return ((char *)s1);
	while (s1[i])
	{
		j = i;
		while (s1[j] == s2[k] && s1[j] && s2[k])
		{
			j++;
			k++;
		}
		if (s2[k] == '\0')
			return ((char *)s1 + i);
		k = 0;
		i++;
	}
	return (0);
}
